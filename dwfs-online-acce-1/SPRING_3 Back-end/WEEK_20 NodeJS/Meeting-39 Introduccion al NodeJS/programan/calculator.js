const sumar = (firstNumber, secondNumber) => {
    return firstNumber + secondNumber;
};

const resta = (firstNumber, secondNumber) => {
    return firstNumber - secondNumber;
};

const multiplicar = (firstNumber, secondNumber) => {
    return firstNumber * secondNumber;
};

const dividir = (firstNumber, secondNumber) => {
    return firstNumber / secondNumber;
};

module.exports = {
    sumar,
    resta,
    multiplicar,
    dividir
};